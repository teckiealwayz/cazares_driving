      </h4>
    </div>
    <div id="collapse11" class="panel-collapse collapse">
      <div class="panel-body">
       Yes we do. We offer private behind the wheel lessons. These private drives begin with an assessment and are tailored to the student’s specific needs. The cost is $40/hour payable at the time the lesson is scheduled. Credit/Debit Cards or Money Orders only (No Cash)
