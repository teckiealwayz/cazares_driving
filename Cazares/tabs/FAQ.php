      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse12">I already completed the 16 days of class, how do I schedule my driving appointments?</a>
      </h4>
    </div>
    <div id="collapse12" class="panel-collapse collapse">
      <div class="panel-body">
       Behind-the- wheel appointments are scheduled online through our website. Once you have been issued a permit from DPS, return to the Cazares location for your username and password. Behind-the- wheel lessons, as part of the Teen Drivers Ed course, can only be done at the location where you took your classroom instruction at. You cannot schedule appointments at other Cazares locations. Driving appointments are posted every Thursday for the following week. We have appointments available Monday – Saturday, times vary by location.
