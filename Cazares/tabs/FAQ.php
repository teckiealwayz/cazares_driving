        <a data-toggle="collapse" data-parent="#accordion" href="#collapse1">How old do I have to be to start driver's education?</a>
      </h4>
    </div>
    <div id="collapse1" class="panel-collapse collapse">
      <div class="panel-body">You can start the classroom phase at 14 and return when you are 15 to sign up for the behind the wheel
program. If you are 15 years of age, you can start the full program, which includes classroom and
behind-the- wheel concurrently. Keep in mind that you must be 15 to get your learner’s permit.​</div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse2">
        How long is the teen driver's education course?</a>
      </h4>
    </div>
    <div id="collapse2" class="panel-collapse collapse">
      <div class="panel-body">The classroom phase consists of 32 hours of classroom (16 days – 2 hours/day) and 7 hours of behind-
the-wheel driving and 7 hours of in-car observation. It is also required that an additional 30 hours of
behind-the- wheel driving be done with a parent or guardian, 10 hours of which must be done at night​.</div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">What do I have to bring the first day of class?</a>
      </h4>
    </div>
    <div id="collapse3" class="panel-collapse collapse">
      <div class="panel-body">You must bring a black-ink pen and paper to class every day. Textbooks and the Texas Drivers Handbook
will be provided in class. You can also download the Texas Driver Handbook online <a href="#">here</a>.</div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">What if I can't attend every lesson?</a>
      </h4>
    </div>
    <div id="collapse4" class="panel-collapse collapse">
      <div class="panel-body">Absences are not encouraged, but the state allows 5 days or 10 hours missed. Keep in mind that all
absences must be made up. These can be made up by attending the same day of instruction with a later
course (at no charge) or complete a make-up packet for the day missed. If you select to do a makeup
packet, just show up an hour before your class, request a packet at the front desk, and begin working on
it. Each makeup packet costs $10 payable at that time. Please note that your balance must be paid in full
to be eligible to make-up any lessons. Only Credit/Debit Cards or money orders accepted.</div>
    </div>
  </div>
    <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse5">How long must teens have a permit before getting a license?</a>
      </h4>
    </div>
    <div id="collapse5" class="panel-collapse collapse">
      <div class="panel-body">Teens must hold on to their permit for at least 6 months and be 16 years of age to be eligible to take the
Official Driving Test. The back of your permit states &quot;Must hold permit until (date). Teens are eligible to
take the Official Driving Test on that date or any day after provided that the Drivers Ed Program
classroom and behind the wheel hours have been completed.</div>
    </div>
  </div>
    <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse6">Do teens still have to take a driving test at the DPS?</a>
      </h4>
    </div>
    <div id="collapse6" class="panel-collapse collapse">
      <div class="panel-body">Cazares Driving School is authorized by the Texas Department of Public Safety to administer the &quot;Official Driving Test&quot; for teens and adults to get a license. Teens who complete the teen course (16 days of classroom, 7 driving hours and 7 observation hours) must watch a mandatory 2 hour &quot;Impact Texas Teen Drivers&quot; video to be eligible to take the Driving Test. You can watch the video online at (link) and bring in your video certificate signed by student and parent. You can also come in to our Cazares McAllen 10 th &amp; Pecan location to watch the video certificate of completion.</div>
    </div>
  </div>
    <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse7">If I am a teen, what do I need to bring to my Driving Test appointment?</a>
      </h4>
    </div>
    <div id="collapse7" class="panel-collapse collapse">
      <div class="panel-body">As a teen please bring the following to your driving test appointment:
        <ol>
          <li>DE964 (Green certificate of completion form) from the driving school you attended your Drive Test appointment.</li>
          <li>Valid, unexpired permit.</li>
          <li>Impact Texas Teen Certificate of Completion.</li>
          <li>Parent with a valid ID (Driver license, Texas ID, Passport, Resident Card, VISA, Consular Card)</li>
          <li>Vehicle with valid insurance.</li>
          <li>$75 Credit/Debit or Money Order (No Cash) or $85 Credit/Debit or Money Order (No Cash) if choosing to test in a Cazares Driving School Vehicle.</li>
        </ol>
      </div>
    </div>
  </div>
 <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse8">What happens if I don't pass my driving test? Will I have to pay again?</a>
      </h4>
    </div>
    <div id="collapse8" class="panel-collapse collapse">
      <div class="panel-body">
        No, the cost for the driving test is $75 for (3) Three drive test opportunities. Your examiner will make suggestions for improvement and you may retest as soon as you are ready. Just make sure your Impact Texas Teen video has not expired. If it does expire, you will need to watch it again and print out a new certificate. The Impact Teen Video certificate is good for 3 months.
      </div>
    </div>
  </div>
   <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapse9">Do I need to be in high school to take driver's education?</a>
      </h4>
    </div>
    <div id="collapse9" class="panel-collapse collapse">
      <div class="panel-body">
       Texas law requires that persons under the age of 18 be enrolled in school and attending regularly to get a license. You will be required to show proof of enrollment in school at DPS via Verification of Enrollment form. There is an exception: if the person has a high school diploma or a GED, the drivers’ license could be issued.
      </div>
    </div>
  </div>
