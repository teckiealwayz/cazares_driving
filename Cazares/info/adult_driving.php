<p style="padding: 10px;">If you are 18 to 24 years old it is now required that you take a 6-hour Adult Driver Education Course. This course will include the Road Rules and Road Signs written tests at our facility. You will not be required to take the written test at the DPS Office. </p>

<p style="padding: 10px;">
After successful completion of the 6-hour Adult Driver Education Course, you will receive a certificate of completion to present at the DPS Office. We highly recommend that all adults even over the age of 24 take this course because not only do we prepare you well for the Road Rules and Road Signs Exams, you take these exams at our facility, in a quiet environment while the information is fresh on your mind. All of our classroom instructors are bilingual and students can choose to take their exams in English or Spanish. Presenting a copy of the 6-Hour Adult Completion Certificate awarded to you at Cazares Driving School may also qualify you for a 10% discount on your auto insurance. 
</p>
